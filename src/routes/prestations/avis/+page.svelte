<article class="mt-20">
	<h3>Points Clés de la Mission</h3>
	<ul>
		<li>
			<span class="font-bold text-gray-900">Analyse minutieuse :</span> Nous inspectons le gros œuvre,
			le second œuvre, l'état de la façade, et la structure de la charpente et de la couverture.
		</li>
		<li>
			<span class="font-bold text-gray-900">Identification des Désordres :</span> Nous identifions les
			désordres et en définissons le niveau de dangerosité, le degré de dégradation, et les besoins de
			réparation.
		</li>
		<li>
			<span class="font-bold text-gray-900">Recherche des Causes Probables :</span> Nous listerons les
			causes probables des désordres en fonction des éléments visibles.
		</li>
		<li>
			<span class="font-bold text-gray-900">Investigations :</span> Nous préconisons des investigations
			complémentaires pour déterminer la cause, si les éléments visibles ne sont pas suffisament probants.
		</li>
		<li>
			<span class="font-bold text-gray-900">Pas de Recherche de Responsabilité :</span> Notre mission
			se concentre sur l'état du bien, sans rechercher ni déterminer de responsabilités.
		</li>
		<li>
			<span class="font-bold text-gray-900">Compte Rendu de Mission :</span> En fonction de la mission
			choisie, vous aurez un avis oral, une note expertale ou un rapport d'expertise avec reportage photos
			et description des désordres identifiés.
		</li>
	</ul>

